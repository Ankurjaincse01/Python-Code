# Examples of Arithmetic Operator
a = int(input("enter a first number "))
b = int(input("ente a second number "))

# Addition of numbers
add = a + b

# Subtraction of numbers
sub = a - b

# Multiplication of number
mul = a * b

# Division(float) of number
div1 = a / b

# Division(floor) of number
div2 = a // b

# Modulo of both number
mod = a % b

# Power
p = a ** b

# print results
print("Additon of number ",add)
print("Substraction of number ", sub)
print("Multiplication of number ",mul)
print("Division(float) of number", div1)
print("Division(floor) of number", div2)
print("Module of both number",mod)
print("Power", p)
